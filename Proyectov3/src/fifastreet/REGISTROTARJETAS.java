    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jComboBox1.getSelectedIndex()!= -1){
            ConexionDB conn=new ConexionDB();
            conn.insertTarjeta((String)jComboBox1.getSelectedItem(), (String)jComboBox2.getSelectedItem(), true, false);

            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).AñadirAmarilla();
            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).setAuxAmarilla(team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).getAuxAmarilla()+1);
            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).CalcularMulta();

            jLabel9.setText("Tarjeta amarilla insertada");
        }
        else{
            jLabel9.setText("No se a seleccionado equipo");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

