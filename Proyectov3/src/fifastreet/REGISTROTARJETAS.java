    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jComboBox1.getSelectedIndex()!= -1){
            ConexionDB conn=new ConexionDB();
            conn.insertTarjeta((String)jComboBox1.getSelectedItem(), (String)jComboBox2.getSelectedItem(), false, true);

            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).AñadirRoja();
            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).setAuxRoja(team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).getAuxRoja()+1);
            team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(jComboBox2.getSelectedIndex()).CalcularMulta();

            jLabel9.setText("Tarjeta roja insertada");
        }
        else{
            jLabel9.setText("No se a seleccionado equipo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

