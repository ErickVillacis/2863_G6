    private void jComboBox1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBox1FocusLost

        for ( numJugadores = 0; numJugadores < team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().size(); numJugadores++) {
            jComboBox2.addItem(team.getEquipos().get(jComboBox1.getSelectedIndex()).getJugadores().get(numJugadores).getNombre());
        }
        jLabel9.setText(" ");
    }//GEN-LAST:event_jComboBox1FocusLost

    private void jComboBox1FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jComboBox1FocusGained
        jComboBox2.removeAllItems();
        jComboBox1.removeAllItems();

        for ( numEquipos = 0; numEquipos < team.getEquipos().size(); numEquipos++) {
            jComboBox1.addItem(team.getEquipos().get(numEquipos).getNombreEquipo());
        }
    }//GEN-LAST:event_jComboBox1FocusGained

